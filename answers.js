//This function takes a string as argument and returns its first character.

function firstCharacter(string) {
    return string[0];
}

//This function takes a string as argument and returns its last character. 

function lastCharacter(string) {
    return string [ string.length - 1 ];
}

//This function takes a string and a number as arguments, and returns the character at the position represented by the number

function letterPosition (string, num) {
    return string[num];
}
